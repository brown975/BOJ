#include <stdio.h>
long long a, s, d[100005][3] = { 0,0,0,1,0,0,0,1,0,1,1,1 };
int main() {
	scanf("%lld", &a);
	for (int g = 4; g <= 100000; g++) {
		d[g][0] = (d[g - 1][1] + d[g - 1][2]) % 1000000009;
		d[g][1] = (d[g - 2][0] + d[g - 2][2]) % 1000000009;
		d[g][2] = (d[g - 3][0] + d[g - 3][1]) % 1000000009;
	}
	while (a--) {
		scanf("%lld", &s);
		printf("%lld\n", (d[s][0] + d[s][1] + d[s][2]) % 1000000009);
	}
}
